package linkedlist;

public class Node{
	private int data;
	private Node nextNode;
	
	public Node(int data) {
		this.data = data;
	}
	
	public int getData() {
		return this.data;
	}
	
	public void setData(int Data) {
		this.data = data;
	}
	
	public Node getNextNode() {
		return this.nextNode;
	}
	
	public void setNextNode() {
		this.nextNode= nextNode;
	}
	
	
}